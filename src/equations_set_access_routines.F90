  !>Returns the equations set specification i.e., equations set class, type and subtype for an equations set. \see OpenCMISS::Iron::cmfe_EquationsSet_SpecificationGet
  SUBROUTINE EquationsSet_SpecificationGet(equationsSet,minSpecificationLength,equationsSetSpecification,err,error,*)

    !Argument variables
    TYPE(EquationsSetType), POINTER :: equationsSet !<A pointer to the equations set to get the specification for
    INTEGER(INTG), INTENT(IN) :: minSpecificationLength !<The minimum number of equations set specification identifiers to return. If the minSpecificationLength is zero then all specification identifiers are required. 
    INTEGER(INTG), INTENT(INOUT) :: equationsSetSpecification(:) !<On return, The equations set specifcation array. Must be allocated on entry.
    INTEGER(INTG), INTENT(OUT) :: err !<The error code
    TYPE(VARYING_STRING), INTENT(OUT) :: error !<The error string
    !Local Variables
    INTEGER(INTG) :: specificationLength
#ifdef WITH_PRECHECKS    
    TYPE(VARYING_STRING) :: localError
#endif    

    ENTERS("EquationsSet_SpecificationGet",err,error,*999)
   
#ifdef WITH_PRECHECKS    
    IF(.NOT.ASSOCIATED(equationsSet)) CALL FlagError("Equations set is not associated.",err,error,*999)
    IF(.NOT.ALLOCATED(equationsSet%specification)) THEN
      localError="The specification array has not been allocated for equations set number "// &
        & TRIM(NumberToVString(equationsSet%userNumber,"*",err,error))
      IF(ASSOCIATED(equationsSet%region)) &
        & localError=localError//" of region number "//TRIM(NumberToVString(equationsSet%region%userNumber,"*",err,error))
      localError=localError//"."
      CALL FlagError(localError,err,error,*999)
    ENDIF
#endif
    IF(minSpecificationLength==0) THEN
      specificationLength=equationsSet%specificationLength
    ELSE
      specificationLength=MAX(minSpecificationLength,equationsSet%specificationLength)
    ENDIF
#ifdef WITH_PRECHECKS
    IF(specificationLength>equationsSet%specificationLength) THEN
      localError="The specification for equations set number "//TRIM(NumberToVString(equationsSet%userNumber,"*",err,error))
      IF(ASSOCIATED(equationsSet%region)) &
        & localError=localError//" of region number "//TRIM(NumberToVString(equationsSet%region%userNumber,"*",err,error))
      localError=localError//" does not have enougth specification identifiers. The specification length is "// &
        & TRIM(NumberToVString(equationsSet%specificationLength,"*",err,error))//" and "// &
        & TRIM(NumberToVString(minSpecificationLength,"*",err,error))//" specification identifiers have been requested."
      CALL FlagError(localError,err,error,*999)
    ENDIF
    IF(SIZE(equationsSetSpecification,1)<specificationLength) THEN
      localError="The equations set specification array size is "// &
        & TRIM(NumberToVstring(SIZE(equationsSetSpecification,1),"*",err,error))// &
        & " and it needs to be >= "//TRIM(NumberToVstring(specificationLength,"*",err,error))//"."
      CALL FlagError(localError,err,error,*999)
    ENDIF
#endif    
        
    equationsSetSpecification(1:specificationLength)=equationsSet%specification(1:specificationLength)

    EXITS("EquationsSet_SpecificationGet")
    RETURN
999 ERRORSEXITS("EquationsSet_SpecificationGet",err,error)
    RETURN 1
    
  END SUBROUTINE EquationsSet_SpecificationGet

